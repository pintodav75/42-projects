#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dest_len;
	size_t	src_len;
	size_t	i;

	dest_len = 0;
	src_len = 0;
	if (size == 0)
		return (0);
	while (dst[dest_len] != '\0' && dest_len < size)
		dest_len++;
	while (src[src_len] != '\0')
		src_len++;
	if (size <= dest_len)
		return (size + src_len);
	i = dest_len;
	while (i < size - 1 && src[i - dest_len] != '\0')
	{
		dst[i] = src[i - dest_len];
		i++;
	}
	dst[i] = '\0';
	return (dest_len + src_len);
}
